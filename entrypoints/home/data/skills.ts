export interface Skill {
  id: string
  name: string
  description: string
  iconUrl?: string
  categories: string[]
  installs: number
  version: string
  official?: boolean
  author: string
}

/** Known brand keywords → abbreviation for icon fallback */
const BRAND_MAP: Record<string, string> = {
  linkedin: 'Li',
  twitter: 'Tw',
  facebook: 'Fb',
  instagram: 'Ig',
  tiktok: 'Tk',
  youtube: 'YT',
  reddit: 'Re',
  pinterest: 'Pi',
  whatsapp: 'WA',
  google: 'G',
  amazon: 'a',
  airtable: 'At',
  notion: 'No',
  slack: 'Sl',
  yandex: 'YM',
}

/** Get icon text for a skill: match known brand or use first 2 letters */
export function getSkillAbbr(name: string): string {
  const lower = name.toLowerCase()
  for (const [keyword, abbr] of Object.entries(BRAND_MAP)) {
    if (lower.includes(keyword)) return abbr
  }
  const words = name.split(/\s+/)
  if (words.length >= 2) return (words[0][0] + words[1][0]).toUpperCase()
  return name.slice(0, 2).toUpperCase()
}

